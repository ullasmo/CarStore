// Fetch all notifications
export const getAllNotificationController = async (req, res) => {
  try {
    const user = await User.findById(req.body.userId);
    res.status(200).json({
      success: true,
      message: "Notifications fetched successfully",
      notifications: {
        unread: user.notification,
        seen: user.seennotification,
      },
    });
  } catch (error) {
    console.error("Error fetching notifications:", error);
    res.status(500).json({
      success: false,
      message: "Error fetching notifications",
      error: error.message,
    });
  }
};

// delete notifications
export const deleteAllNotificationController = async (req, res) => {
  try {
    const user = await User.findOne({ _id: req.body.userId });
    user.notification = [];
    user.seennotification = [];
    const updatedUser = await user.save();
    updatedUser.password = undefined;
    res.status(200).send({
      success: true,
      message: "Notifications Deleted successfully",
      data: updatedUser,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      message: "unable to delete all notifications",
      error,
    });
  }
};

// Mark all notifications as read
export const markAllNotificationAsReadController = async (req, res) => {
  try {
    const user = await User.findById(req.body.userId);
    // Move all notifications to the 'seennotification' array
    user.seennotification.push(...user.notification);
    user.notification = []; // Clear the 'notification' array
    const updatedUser = await user.save();
    res.status(200).json({
      success: true,
      message: "All notifications marked as read",
      updatedUser,
    });
  } catch (error) {
    console.error("Error marking all notifications as read:", error);
    res.status(500).json({
      success: false,
      message: "Error marking all notifications as read",
      error: error.message,
    });
  }
};
